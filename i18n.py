def localize_log(text):
    """Перевести строку лога на текущий язык (для не-ru). Применяется на ВЫВОДЕ, после
    русско-зависимой маршрутизации. Сейчас цель — английский (прочие локали -> en)."""
    if not text or _lang() == "ru-RU":
        return text
    s = text
    for rx, rep in _LOG_RE:
        s = rx.sub(rep, s)
    for ru, en in _LOG_SUBS:
        if ru in s:
            s = s.replace(ru, en)
    return s


