def _lang():
    try:
        return json.load(open(CFG, encoding="utf-8")).get("lang_main", "ru-RU")
    except Exception:
        return "ru-RU"


def t(key, **kw):
    d = LANG.get(key, {})
    loc = _lang()
    s = d.get(loc) or d.get("en-US") or d.get("ru-RU") or key
    try:
        return s.format(**kw) if kw else s
    except Exception:
        return s


