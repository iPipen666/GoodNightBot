_GRADE_KEY = {
    "обычный": "Grade_COMMON", "необычный": "Grade_UNCOMMON", "редкий": "Grade_RARE",
    "легендарный": "Grade_LEGENDARY", "бессмертный": "Grade_IMMORTAL", "аркана": "Grade_ARCANA",
    "запредельный": "Grade_BEYOND", "celestial": "Grade_CELESTIAL",
    "божественный": "Grade_DIVINE", "космический": "Grade_COSMIC",
}


def _load_grade_names():
    try:
        bk = json.load(open(os.path.join(HERE, "game_strings_i18n.json"), encoding="utf-8"))["by_key"]
        return {tok: bk.get(key, {}) for tok, key in _GRADE_KEY.items()}
    except Exception:
        return {}


GRADE_NAMES = _load_grade_names()


def grade_name(token):
    """Локализованное имя грейда (из игровых строк, 16 языков). Фолбэк: en-US -> сам токен."""
    d = GRADE_NAMES.get(token, {})
    loc = _lang()
    return d.get(loc) or d.get("en-US") or token


def st(text):
    """Перевод строки настроек по её русскому исходнику. Сам text — это ru-RU оригинал,
    поэтому для ru-RU (и неизвестных локалей без записи) возвращаем его. Иначе locale -> en-US -> text."""
    loc = _lang()
    if loc == "ru-RU":
        return text
    d = _ST.get(text)
    if not d:
        return text
    return d.get(loc) or d.get("en-US") or text
